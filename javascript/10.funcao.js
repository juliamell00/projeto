// Criando uma função
function somar(a, b) {
    return a + b;
}

function subtrirair(a, b) {
    return a - b;
}
function multiplicar(a, b) {
    return a * b;
}
function dividir(a, b) {
    return a / b;
}


// Chamando a função//
const soma = somar(3,3)
const subtracao = subtrirair(3,3)
const multiplicacao = multiplicar(3,3)
const divisao = dividir(3,3)

//mostra o conteúdo da constante 
console.log(`Soma: ${soma}`)
console.log(`Subtração: ${subtracao}`)
console.log(`Multiplicação: ${multiplicacao}`)
console.log(`Divisão: ${divisao}`)

